package jan06;

public class Calculate {


    int add(int a , int b ){
        return a+b;
    }

    double add(double a , int b ){
        return a+b;
    }

    int add(int a , int b , int c){
        return a+b+c;
    }

    int add(int a , int b , int c , int d){
        return a+b+c+d;
    }

    double add(double a , double b){
        return a+b;
    }





}
