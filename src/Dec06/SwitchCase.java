package Dec06;

public class SwitchCase {

    public static void main(String[] args) {
        int a =10;

        switch (a){
            case 10 :
                System.out.println("Num is 10");
                break;
            case 20:
                System.out.println("Num is 20");
                break;
            default:
                System.out.println("Can't say what is the number");
        }
    }
}
